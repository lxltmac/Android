package lin.linorder.models.order;

public class OrderList {
	private int id;
	private String tableNum;
	
	private OrderList(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTableNum() {
		return tableNum;
	}

	public void setTableNum(String tableNum) {
		this.tableNum = tableNum;
	}
	

}
